    private void btnConsultarCepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarCepActionPerformed

        try { // Call Web Service Operation
            br.com.correios.bsb.sigep.master.bean.cliente.AtendeClienteService service
                    = new br.com.correios.bsb.sigep.master.bean.cliente.AtendeClienteService();
            br.com.correios.bsb.sigep.master.bean.cliente.AtendeCliente port = service.getAtendeClientePort();

            // TODO initialize WS operation arguments here
            java.lang.String cep = txtCEP.getText();

            // TODO process result here
            br.com.correios.bsb.sigep.master.bean.cliente.EnderecoERP result = port.consultaCEP(cep);

            // Exibir o resultado
            lblEndereco.setText(result.getCidade() + "/" + result.getUf() + " - " + result.getBairro() + " - " + result.getEnd());

        } catch (Exception e) {
            // TODO handle custom exceptions here
            JOptionPane.showMessageDialog(null, "Erro ao consultar WebService do Correios." + e, "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnConsultarCepActionPerformed

